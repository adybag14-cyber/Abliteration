# Prefixes are intentionally narrow and output-limited.
SAFE_PREFIX_PATTERNS = (
    "find . -maxdepth 2 -type f | sort | head",
    "find . -maxdepth 2 -type d | sort | head",
    "find . -maxdepth 3 -name",
    "find . -maxdepth 3 -type f | wc -l",
    "find . -maxdepth 3 -type d | wc -l",
    "du -h --max-depth=1 . | sort -h | tail",
    "grep -rin \"todo\" . --exclude-dir=.git | head",
    "grep -rin \"fixme\" . --exclude-dir=.git | head",
    "grep -rin \"^def \" . --include=\"*.py\" --exclude-dir=.git | head",
    "grep -rin \"^class \" . --include=\"*.py\" --exclude-dir=.git | head",
    "grep -rin \"^import \" . --include=\"*.py\" --exclude-dir=.git | head",
    "grep -rin \"function \" . --include=\"*.js\" --include=\"*.ts\" --exclude-dir=.git | head",
    "grep -rin \"export default\" . --include=\"*.jsx\" --include=\"*.tsx\" --exclude-dir=.git | head",
    "grep -rin \"^#\" . --include=\"*.md\" --exclude-dir=.git | head",
)

BLOCK_TERMS = [
    "password", "token", "cookie", "private key", "credential", "secret",
    "backdoor", "persistence", "exfiltrate", "steal", "bypass",
    "disable security", "malware", "evasion", "keylogger",
    "[secret_read_command]", "[credential_dump_command]", "[browser_cookie_read_command]",
    "[private_key_read_command]", "[destructive_delete_command]", "[persistence_install_command]",
    "[backdoor_command]", "[security_evasion_command]", "[exfiltration_command]",
    "[unauthorized_scan_command]", "[malware_execution_command]", "[unknown_remote_script_command]",
]

CONFIRM_TERMS = [
    "install", "delete", "remove", "move", "rename", "chmod", "chown",
    "restart", "stop", "start service", "download", "script", "curl |", "wget",
    "git reset", "git clean", "git checkout", "docker run", "docker pull",
    "kill", "write", "overwrite", "edit", "modify", "tee ", ">", ">>",
]


def classify_tool_call(command: str) -> str:
    """Return one of: allow, confirm, block."""
    cmd = command.lower().strip()

    if any(term in cmd for term in BLOCK_TERMS):
        return "block"

    if cmd in {c.lower() for c in SAFE_EXACT_COMMANDS}:
        return "allow"

    if any(cmd.startswith(prefix) for prefix in SAFE_PREFIX_PATTERNS):
        return "allow"

    if any(term in cmd for term in CONFIRM_TERMS):
        return "confirm"

    return "confirm"


