#!/usr/bin/env node
/**
 * Build docs/tools/heretic-models-registry.md from data/heretic-models-registry.jsonl
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const root = join(__dir, '..');
const REGISTRY = join(root, 'data', 'heretic-models-registry.jsonl');
const OUT = join(root, 'docs', 'tools', 'heretic-models-registry.md');

function loadRows() {
  return readFileSync(REGISTRY, 'utf8')
    .trim()
    .split('\n')
    .filter(Boolean)
    .map((line) => JSON.parse(line));
}

function fmtDownloads(n) {
  if (n == null) return '—';
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(1)}M`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(1)}k`;
  return String(n);
}

function table(rows, title) {
  if (rows.length === 0) return `### ${title}\n\n_None._\n`;
  const lines = [
    `### ${title}`,
    '',
    '| Model | Status | Open | Downloads | Notes |',
    '|-------|--------|------|-----------|-------|',
  ];
  for (const r of rows) {
    const open = r.open_weights ? 'yes' : r.gated ? 'gated' : 'no';
    const notes = (r.handbook_notes || r.base_model || '').replace(/\|/g, '/').slice(0, 80);
    lines.push(
      `| [${r.model_id}](${r.url}) | ${r.status || '—'} | ${open} | ${fmtDownloads(r.downloads)} | ${notes || '—'} |`,
    );
  }
  return lines.join('\n') + '\n';
}

function main() {
  const rows = loadRows();
  const handbook = rows.filter((r) => r.attempted_by === 'abliteration-handbook' || r.seed);
  const official = rows.filter((r) => r.attempted_by === 'p-e-w' && r.status === 'published_heretic');
  const community = rows.filter(
    (r) =>
      !handbook.includes(r) &&
      !official.includes(r) &&
      (r.heretic_tag || r.abliterated_tag || r.status === 'community_abliterated'),
  );
  const openCount = rows.filter((r) => r.open_weights).length;
  const fetchedAt = rows.find((r) => r.fetched_at)?.fetched_at || 'unknown';

  const md = `<!-- generated by scripts/build-heretic-models-doc.mjs — do not edit by hand -->

# Heretic / abliterated models (Hugging Face)

Open-weight models tagged **heretic** or **abliterated** on Hugging Face, plus handbook-recommended bases.

**Registry:** [../../data/heretic-models-registry.jsonl](../../data/heretic-models-registry.jsonl) (${rows.length} rows, ${openCount} open weights)  
**Last HF scrape:** ${fetchedAt}  
**Refresh:** \`npm run fetch:hf-models\` or \`npm run fetch:hf-models:firefox\` (see [heretic-tools-reference.md](heretic-tools-reference.md))  
**Browse live:** [huggingface.co/models?other=heretic](https://huggingface.co/models?other=heretic)

---

## Handbook bases (attempt before you publish)

${table(
  handbook.sort((a, b) => (a.model_id || '').localeCompare(b.model_id || '')),
  'Recommended starting checkpoints',
)}

## Official p-e-w Heretic outputs

${table(official.sort((a, b) => (b.downloads || 0) - (a.downloads || 0)), 'Upstream Heretic releases')}

## Community heretic / abliterated (top 50 by downloads)

${table(
  community.sort((a, b) => (b.downloads || 0) - (a.downloads || 0)).slice(0, 50),
  'Community models',
)}

_Full list: \`data/heretic-models-registry.jsonl\` — regenerate with \`node scripts/build-heretic-models-doc.mjs\` after fetch._
`;

  writeFileSync(OUT, md);
  console.log(`Wrote ${OUT} (${rows.length} registry rows)`);
}

main();